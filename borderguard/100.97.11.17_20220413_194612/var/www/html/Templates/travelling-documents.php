<h2>Entry refusal</h2>
<p>In very rare cases, an EU country can refuse entry to you or your family members for reasons of " public policy, public security or public health".
This means the authorities must prove you or your family members pose a " genuine, present and sufficiently serious threat".
You are entitled to receive this decision in writing, stating all the grounds, and specifying how you can appeal and by when.
<p>If you know you have trouble with your Passports (expired document etc..) you can set an <a href='/appointment'>appointment</a> in order to renew it and travel secure.
