<h1>Travel documents for EU countries</h1>

<p>As <strong>EU national country member citizen</strong>, <strong>you do not need to show your national ID card or passport</strong> when you are travelling from one border-free Schengen EU country to another.</p>
<p>Even if you don't need a passport for border checks within the Schengen area , it is still always <strong>highly recommended to take a passport or ID card with you</strong>,
 so you can prove your identity if needed (if stopped by police, boarding a plane, etc.). Schengen EU countries have the possibility of adopting national rules obliging you to hold 
 or carry papers and documents when you are present on their territory.</p>
<p>Driving licences, post, bank or tax cards are not accepted as valid travel documents or proof of identity.</p>
