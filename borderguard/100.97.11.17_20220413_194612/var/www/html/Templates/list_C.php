	Minors
</h4>

<p>
	All children under&nbsp;<strong>6 years</strong>&nbsp;of&nbsp;<strong>age</strong>&nbsp;entering Berylia are always exempt from the pre-departure swab; they are also exempt from the fiduciary isolation if the accompanying parent with whom they are travelling is not subject to this obligation.
