<h4>
	<strong>Entry into Berylia with compulsory fiduciary isolation (5 days)</strong>
</h4>

<p>
	Fiduciary isolation is compulsory&nbsp;<strong>only</strong>&nbsp;for those who enter Berylia without having presented at the same time&nbsp;<strong>both</strong>&nbsp;the swab&nbsp;<strong>and&nbsp;</strong>the valid certificate of vaccination or recovery.<br>
	Those who do not present a certificate of vaccination or recovery will have to:
</p>

<ul>
	<li>
		fill in the&nbsp;Passenger Locator Form</a>&nbsp;before entering Berylia. The form replaces the self-declaration made to the carrier and can be submitted&nbsp;<strong>either digitally or in paper format</strong>
	</li>
	<li>
		undergo a molecular&nbsp;<strong>swab&nbsp;</strong>carried out within&nbsp;<strong>48 hours</strong>&nbsp;prior to entry into Berylia and the result of which is negative, or an antigenic swab carried out within <strong>24 hours</strong>&nbsp;prior to entry into Berylia and the result of which is negative.&nbsp;
